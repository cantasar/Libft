#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t size)
{
	size_t	i;

	i = 0;
	while (i < size && (s1[i] != '\0' || s2[i] != '\0'))
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int main(int argc, char const *argv[])
{
	char a[] = "azbc";
	char b[] = "azbc";
	printf("%d\n", ft_strncmp(a,b,6));
	printf("%d", strncmp(a,b,6));
	return 0;
}
